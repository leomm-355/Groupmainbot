    # Random တစ်ခု ရွေးမယ်
    selected_msg = random.choice(all_messages)
    
    # ရလဒ် ထုတ်ပြမယ်
    response_text = (
        f"💖 **Love Horoscope for {message.from_user.mention}** 💖\n"
        "━━━━━━━━━━━━━━━━━━\n\n"
        f"_{selected_msg}_\n\n"
        "━━━━━━━━━━━━━━━━━━\n"
        "✨ ကံကြမ္မာဆိုတာ ကိုယ့်လက်ထဲမှာပဲ ရှိတာပါနော် ✨"
    )
    
    await message.reply_text(response_text)
